package spring.turbo.bean;

import org.springframework.lang.Nullable;

import java.util.function.Supplier;

/**
 * 工厂
 *
 * @param <T> 产品类型
 * @author 应卓
 * @since 1.0.0
 */
@FunctionalInterface
public interface Factory<T> extends Supplier<T> {

    @Nullable
    public T create();

    @Override
    @Nullable
    public default T get() {
        return create();
    }

}
